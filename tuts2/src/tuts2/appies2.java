package tuts2;

public class appies2 {
	public static void main(String[] args) {
		//primitive type 
		int myInt = 56;
		
		//when vars aren't primitive, they are capitalized and are actually classes
		String text = "Hello";
		String textTwo = "my friend";
		String textThree = "Bobby!";
		
		String greeting = text + textTwo + textThree;
		System.out.println(greeting);
		System.out.println("hello" + " " + "you poon!");
		System.out.println("my int is: " + myInt);
		
		double myDouble = 7.8;
		System.out.println("my num is: " + myDouble + "!");
	}
}
